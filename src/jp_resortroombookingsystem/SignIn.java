    int cnt = 3;
    private void signinButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinButActionPerformed
        // TODO add your handling code here:
        String staffID, password;
        if (cnt != 0 ){
            while (true){
                try {
                    staffID = EnterTxt1.getText();
                    break;
                }
                catch (NullPointerException ne){
                    JOptionPane.showMessageDialog(null, "Please enter your Staff ID","Error Message",JOptionPane.ERROR_MESSAGE);

                }
                catch (Exception e){
                    JOptionPane.showMessageDialog(null, "","Error Message",JOptionPane.ERROR_MESSAGE);
                }
            }
            
            while(true){
                try {
                    password = EnterTxt2.getText();
                    break;
                }
                catch (NullPointerException ne){
                    JOptionPane.showMessageDialog(null, "Please enter your password","Error Message",JOptionPane.ERROR_MESSAGE);
                }
                catch (Exception e){
                    JOptionPane.showMessageDialog(null, "","Error Message",JOptionPane.ERROR_MESSAGE);
                }
            }
            
            if (staffID.contains("Admin") && password.contains("password"))
            {
                JOptionPane.showMessageDialog(null, "Login Successfully!");
                Menu m = new Menu();
                m.setVisible(true);
                dispose();
            } 
            else{
                EnterTxt1.setText("");
                EnterTxt2.setText("");
                cnt = cnt - 1;
                JOptionPane.showMessageDialog(null, "Incorrect username or password.\n"+cnt+" attempt(s) left.","Login failed", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "0 login attempt left. \nPlease contact admin to login.","Login failed", JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_signinButActionPerformed

