    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        // TODO add your handling code here:
        //set all buttons enable
        Component[] components = getContentPane().getComponents();
        for (Component component : components) {
            if (component instanceof JButton) {
                JButton specificObject = (JButton) component;
                specificObject.setEnabled(true);
            }
        }
        
        //filter room by room view
        String RoomView = view.getSelectedItem().toString();
        
        if (RoomView == "Jungle"){
            RoomTxt1.setText("R01");
            RoomTxt2.setText("R02");
            RoomTxt3.setText("R03");
            RoomTxt4.setText("R04");
            RoomTxt5.setText("R05");
            RoomTxt6.setText("R06");
            RoomTxt7.setText("R07");
            RoomTxt8.setText("R08");
            RoomTxt9.setText("R09");
            RoomTxt10.setText("R10");
        }
        else{
            RoomTxt1.setText("R11");
            RoomTxt2.setText("R12");
            RoomTxt3.setText("R13");
            RoomTxt4.setText("R14");
            RoomTxt5.setText("R15");
            RoomTxt6.setText("R16");
            RoomTxt7.setText("R17");
            RoomTxt8.setText("R18");
            RoomTxt9.setText("R19");
            RoomTxt10.setText("R20");
        }
        
        //Get date d
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        Date d = null;
        try{
            d = f.parse(f.format(jCalendar.getDate()));
            Checkin.setDate(d);
            // read file and get record for the selected date
            try(BufferedReader r = new BufferedReader(new FileReader("Booking.txt"))){
                String line = r.readLine();
                while(line != null){
                    Date d1 = f.parse(line.split(";")[6]);
                    Date d2 = f.parse(line.split(";")[7]);
                    Component[] components2 = getContentPane().getComponents();
                    for (Component component2 : components2) {
                        if (component2 instanceof JButton) {
                            JButton specificObject = (JButton) component2;
                            if (specificObject.getText().equals(line.split(";")[5])){
                                if ((d.equals(d1) || d.equals(d2)) ||(d.after(d1) && d.before(d2))){
                                    specificObject.setEnabled(false);
                                }
                            }
                        }
                    }
                    line = r.readLine();
                }
                r.close();
            }
            catch (IOException ex){
                ex.printStackTrace();
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_showButtonActionPerformed

