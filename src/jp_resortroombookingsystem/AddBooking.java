    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        int flag = 1;
        Date checkoutD = null; 
        try{
            //get inputs
            String bookingid = BookID.getText();
            String roomid = RoomID.getText().trim();
            String name = CustName.getText().trim();
            String ICno = IC.getText().trim();
            String contactno = ContactNo.getText().trim();
            String email = Email.getText().trim();
            Date checkinDate = Checkin.getDate();
            Date checkoutDate = Checkout.getDate();

            //format date to string
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            String checkin = f.format(checkinDate);
            String checkout = f.format(checkoutDate);

            //find days of stay
            long DiffBetween = checkoutDate.getTime()  - checkinDate.getTime();
            long DaysBetween = TimeUnit.DAYS.convert(DiffBetween, TimeUnit.MILLISECONDS);
            
            //check if the date is not booked
            try(BufferedReader r = new BufferedReader(new FileReader("Booking.txt"))){
                String line = r.readLine();
                Date d1 = null;
                Date d2 = null; 
                while(line != null){
                    try{
                        d1 = f.parse(line.split(";")[6]);
                        d2 = f.parse(line.split(";")[7]);
                        checkoutD = f.parse(checkout);       
                    }
                    catch(ParseException p){
                        p.printStackTrace();
                    }
                    
                    //check if the date is not booked or checkout date not before checkin date
                    if (checkoutDate.before(checkinDate)){
                        flag = 0;
                        break;
                    }
                    else if (roomid.equals(line.split(";")[5])&& !bookingid.equals(line.split(";")[0])){
                         if ((checkoutD.equals(d1) || checkoutD.equals(d2)) ||(checkoutD.after(d1) && checkoutD.before(d2))) 
                         {   
                            flag = 0;
                            break;
                        }
                    }        
                    line = r.readLine();
                }
                r.close();
            }
            catch (IOException ex){
                ex.printStackTrace();
            }
            
            if (flag == 1){
                //write into file
                try (PrintWriter writer = new PrintWriter(new BufferedWriter (new FileWriter ("Booking.txt",true))))
                {
                    writer.print(bookingid+";");
                    writer.print(name+";");
                    writer.print(ICno+";");
                    writer.print(contactno+";");
                    writer.print(email+";");
                    writer.print(roomid+";");
                    writer.print(checkin+";");
                    writer.print(checkout+";");
                    writer.println(DaysBetween);
                    writer.close();
                    JOptionPane.showMessageDialog(null, "Record added successfully!");

                    //clear text field 
                    RoomID.setText("");
                    CustName.setText("");
                    IC.setText("");
                    ContactNo.setText("");
                    Email.setText("");
                    Checkin.setDate(null);
                    Checkout.setDate(null);

                    //get new book id
                    AutoIncrementID ai = new AutoIncrementID();
                    ai.setData("Booking.txt");
                    int bookid = ai.getdata();
                    BookID.setText(""+bookid);

                }
                catch(IOException ioe){
                    ioe.printStackTrace();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Room is booked on the chosen date or checkout date is set before checkin date.", "Error Message", JOptionPane.ERROR_MESSAGE);
            } 
            
        }
        catch (NullPointerException ne){
            JOptionPane.showMessageDialog(null, "Please fill up all field");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Please ensure that valid inputs are provided.", "Error Message", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_AddBtnActionPerformed

